    private void brnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_brnGuardarActionPerformed
        // Recolección y validación de datos
        String nombre = txtNombre.getText();
        
        // Validación básica
        if (nombre.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Todos los campos (Nombre) son obligatorios.", "Error de Validación", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        List<Object> values = new ArrayList<Object>();
            values.add(nombre);

        // Llamar al método de inserción del DAO
        boolean exito = dao.InsertIntoTable("Habitat", values);

        // Manejo de la respuesta
        if (exito) {
            JOptionPane.showMessageDialog(this, "Habitat guardado exitosamente.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
            cargarDatosIniciales();
            limpiarCampos();
        } else {
            JOptionPane.showMessageDialog(this, "Fallo al guardar la habitat. Revisa la consola para más detalles.", "Error de Inserción", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_brnGuardarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // Verificar si se ha seleccionado alguna fila en la tabla
        int filaSeleccionada = jTable1.getSelectedRow();

        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(this, 
                "Por favor, selecciona el habitat de la tabla que deseas eliminar.", 
                "Selección Requerida", 
                JOptionPane.WARNING_MESSAGE
            );
            return;
        }

        // Obtener el ID del Animal de la fila seleccionada
        // El ID se encuentra en la columna 0 del modelo de la tabla.
        // Usamos getValueAt y convertimos el objeto a entero (Integer)
        Object idValue = jTable1.getModel().getValueAt(filaSeleccionada, 0);
        int animalID = (idValue instanceof Integer) ? (int) idValue : Integer.parseInt(idValue.toString());

        if (animalID == -1) return;

        // Confirmación del Usuario (Práctica recomendada)
        int confirmacion = JOptionPane.showConfirmDialog(this,
            "¿Estás seguro de que deseas eliminar la habitat con ID: " + animalID + "?",
            "Confirmar Eliminación",
            JOptionPane.YES_NO_OPTION
        );

        if (confirmacion != JOptionPane.YES_OPTION)
            return; // El usuario canceló la operación

        // Llamar al método de eliminación del DAO
        boolean exito = dao.DeleteFromTable("Habitat", animalID);

        // Manejo de la respuesta
        if (exito) {
            JOptionPane.showMessageDialog(this, 
                "Habitat eliminado exitosamente.", 
                "Éxito", 
                JOptionPane.INFORMATION_MESSAGE
            );
            // Recargar la tabla para reflejar el cambio
            cargarDatosIniciales();
            limpiarCampos();
        } else {
            JOptionPane.showMessageDialog(this, 
                "Fallo al eliminar el habitat. Revisa la consola para más detalles.", 
                "Error de Eliminación", 
                JOptionPane.ERROR_MESSAGE
            );
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // Verificar si se ha seleccionado alguna fila en la tabla
        int filaSeleccionada = jTable1.getSelectedRow();
        
        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(this, 
                "Debe seleccionar un habitat de la tabla (haciendo clic) para modificar.", 
                "Selección Requerida", 
                JOptionPane.WARNING_MESSAGE
            );
            return;
        }
        
        Object idValue = jTable1.getModel().getValueAt(filaSeleccionada, 0);
        int animalID = (idValue instanceof Integer) ? (int) idValue : Integer.parseInt(idValue.toString());
                                   
        // Recolección y validación de datos
        String nombre = txtNombre.getText();
        
        // Validación básica
        if (nombre.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Todos los campos (Nombre) son obligatorios.", "Error de Validación", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        
        // Confirmación del Usuario (Opcional, pero buena práctica)
        int confirmacion = JOptionPane.showConfirmDialog(this,
            "¿Estás seguro de que deseas modificar la habitat con ID: " + animalID + "?",
            "Confirmar Modificación",
            JOptionPane.YES_NO_OPTION);

        if (confirmacion == JOptionPane.NO_OPTION)
            return; // El usuario canceló la operación

        List<Object> values = new ArrayList<Object>();
            values.add(nombre);

        // Llamar al método de modificación del DAO
        boolean exito = dao.ModifyFromTable("Habitat", animalID, values);

        // 5. Manejo de la respuesta
        if (exito) {
            JOptionPane.showMessageDialog(this, 
                "Habitat modificado exitosamente.", 
                "Éxito", 
                JOptionPane.INFORMATION_MESSAGE);
            cargarDatosIniciales(); // Recargar la tabla
            limpiarCampos();        // Limpiar formulario
        } else {
            JOptionPane.showMessageDialog(this, 
                "Fallo al modificar el habitat.", 
                "Error de Modificación", 
                JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() == 1) { 

            int fila = jTable1.getSelectedRow();
            if (fila == -1) return;

            try {
                Object idValue = jTable1.getModel().getValueAt(fila, 0);
                animalIDSeleccionado = (idValue instanceof Integer) ? (int) idValue : Integer.parseInt(idValue.toString());

                if (animalIDSeleccionado == -1) return;
                
                List<Object> datosAnimal = dao.GetFromIdentifier("Habitat", animalIDSeleccionado);
                if (datosAnimal != null) {
                    llenarCampos(datosAnimal);
                    System.out.println("Datos del Habitat ID " + animalIDSeleccionado + " cargados desde DB.");
                } else
                    System.err.print("No se pudieron obtener los detalles de la habitat desde la base de datos.");
            } catch (Exception e) {
                System.err.print("Error grave al procesar la seleccion, en jTable1MouseClicked: ");
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked
    
