    public static String normalizeForRegex(String input) {
        if (input == null) return "";
        // Normalize to NFD form and strip diacritical marks
        String normalized = Normalizer.normalize(input, Normalizer.Form.NFD)
                                       .replaceAll("\\p{M}", "");
        return normalized;
    }

    private void btnFiltrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFiltrarActionPerformed
        TableModel model = jTable1.getModel();
        
        TableRowSorter<DefaultTableModel> sorter = new TableRowSorter<>((DefaultTableModel) model);
        jTable1.setRowSorter(sorter);

        String nombre = normalizeForRegex(txtNombreFiltro.getText());
        
        // Create individual filters
        RowFilter<DefaultTableModel, Object> nameFilter    = RowFilter.regexFilter("(?i)" + nombre, 1);

        // Combine them with AND logic
        List<RowFilter<DefaultTableModel, Object>> filters = new ArrayList<>();
        if (!nombre.isBlank()) filters.add(nameFilter);
        
        RowFilter<DefaultTableModel, Object> combinedFilter = RowFilter.andFilter(filters);
        sorter.setRowFilter(combinedFilter);
    }//GEN-LAST:event_btnFiltrarActionPerformed

