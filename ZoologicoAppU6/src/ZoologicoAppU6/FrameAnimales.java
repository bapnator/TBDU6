    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() == 1) { 

            int fila = jTable1.getSelectedRow();
            if (fila == -1) return;

            try {
                Object idValue = jTable1.getModel().getValueAt(fila, 0);
                animalIDSeleccionado = (idValue instanceof Integer) ? (int) idValue : Integer.parseInt(idValue.toString());

                if (animalIDSeleccionado == -1) return;
                
                List<Object> datosAnimal = dao.GetFromIdentifier("Animales", animalIDSeleccionado);
                if (datosAnimal != null) {
                    llenarCampos(datosAnimal);
                    System.out.println("Datos del Animal ID " + animalIDSeleccionado + " cargados desde DB.");
                } else
                    System.err.print("No se pudieron obtener los detalles del animal desde la base de datos.");
            } catch (Exception e) {
                System.err.print("Error grave al procesar la seleccion, en jTable1MouseClicked: ");
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

