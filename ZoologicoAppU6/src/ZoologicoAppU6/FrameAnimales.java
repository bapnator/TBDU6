    private void cargarDatosIniciales() {
        dao = new QueryLoaderScript();
        
        // Cargar datos en la JTable (jTable1)
        jTable1.setModel(dao.CargarModeloDinamico("Animales"));

        // Cargar datos en los ComboBox;
        Vector<String> especies = new Vector<String>();
        Vector<String> habitats = new Vector<String>();
        
        especies.add("Seleccionar...");
        especies.addAll(dao.CargarSeleccionDinamica("Especies", "NombreEspecie"));
        cmbEspecie.setModel(new javax.swing.DefaultComboBoxModel<>(especies));

        habitats.add("Seleccionar...");
        habitats.addAll(dao.CargarSeleccionDinamica("Habitat", "NombreHabitat"));
        cmbHabitat.setModel(new javax.swing.DefaultComboBoxModel<>(habitats));
        
        // Puedes usar cmbEspecie1 para el campo "Sexo", si es el caso, llenándolo manualmente:
        cmbSexo.setModel(new javax.swing.DefaultComboBoxModel<>(new String[]{"Seleccionar...", "M", "F"}));
        
        Vector<String> especiesID = new Vector<String>(dao.CargarSeleccionDinamica("Especies", "idEspecie"));
        Vector<String> habitatsID = new Vector<String>(dao.CargarSeleccionDinamica("Habitat", "idHabitat"));
        
        especiesMap = new HashMap();
        habitatsMap = new HashMap();
        
        for (int i = 0; i < especiesID.size(); i++) {
            String id = especiesID.get(i);
            String nombre = especies.get(i + 1);
            especiesMap.put(nombre, id);
        }
        for (int i = 0; i < habitatsID.size(); i++) {
            String id = habitatsID.get(i);
            String nombre = habitats.get(i + 1);
            habitatsMap.put(nombre, id);
        }

        // NOTA IMPORTANTE: Si quieres que la tabla se actualice después de GUARDAR/ELIMINAR,
        // solo tienes que llamar de nuevo a este método: cargarDatosIniciales();
    }

    private void limpiarCampos() {
        // Limpiar campo de texto
        txtNombre.setText("");

        // Resetear ComboBox a la primera opción ("Selecciona")
        cmbSexo.setSelectedIndex(0); // Sexo
        cmbEspecie.setSelectedIndex(0);  // Especie
        cmbHabitat.setSelectedIndex(0);  // Hábitat

        // Resetear las fechas a la fecha actual
        dateCFNacimiento.setCurrent(java.util.Calendar.getInstance()); 
        dateCFRegistro.setCurrent(java.util.Calendar.getInstance());
    }
    private void llenarCampos(List<Object> values) {
        // Limpiar campo de texto
        txtNombre.setText(values.get(2).toString());

        // Seleccionar los valores de los ComboBox
        cmbSexo.setSelectedItem(values.get(3).toString()); 
        cmbEspecie.setSelectedItem(values.get(1).toString());  // Especie
        cmbHabitat.setSelectedItem(values.get(6).toString());  // Hábitat

        // Resetear las fechas a la fecha actual
        dateCFNacimiento.setCurrent(java.util.Calendar.getInstance()); 
        dateCFRegistro.setCurrent(java.util.Calendar.getInstance());

        java.sql.Date sqlDateReg = (java.sql.Date) values.get(4);
        java.util.Calendar calReg = java.util.Calendar.getInstance();
        if(sqlDateReg != null) calReg.setTime(sqlDateReg);
        else calReg.setTime(new java.util.Date());
        dateCFRegistro.setCurrent(calReg);
        dateCFRegistro.setSelectedDate(calReg);

        java.sql.Date sqlDateNac = (java.sql.Date) values.get(5);
        java.util.Calendar calNac = java.util.Calendar.getInstance();
        if(sqlDateNac != null) calNac.setTime(sqlDateNac);
        else calNac.setTime(new java.util.Date());
        dateCFNacimiento.setCurrent(calNac);
        dateCFNacimiento.setSelectedDate(calNac);
    }

